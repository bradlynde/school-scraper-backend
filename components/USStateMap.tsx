const COLORS = {
  notScraped: "#e2e5ea",
  churchOnly: "#1e3a5f",
  schoolOnly: "#6b8e23",
  both: "#2d6a4f",
};

function getStateColor(stateId: string, stateData: Record<string, StateData>): string {
  const data = stateData[stateId];
  if (!data) return COLORS.notScraped;
  const hasChurch = !!data.churchRun;
  const hasSchool = !!data.schoolRun;
  if (hasChurch && hasSchool) return COLORS.both;
  if (hasChurch) return COLORS.churchOnly;
  if (hasSchool) return COLORS.schoolOnly;
  return COLORS.notScraped;
}

function formatDate(dateStr: string): string {
  try {
    const d = new Date(dateStr);
    return d.toLocaleDateString("en-US", { month: "short", day: "numeric", year: "numeric" });
  } catch {
    return dateStr;
  }
}

